package org.example.trihards.geometry2d;

/**
 * The interface for 2D polygons.
 */
public interface Polygon {

    /**
     * The perimeter of the polygon
     *
     * @return the perimeter
     */
    int perimeter();

    /**
     * The area of the polygon
     *
     * @return the area
     */
    int area();
}
